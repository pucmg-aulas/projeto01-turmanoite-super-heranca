public class PropietarioReserva {
    private String nome;
    private String telefone;
    private String cpf;
    private int totalPessoas;

    public PropietarioReserva(String nome, String telefone, String cpf, int totalPessoas) {
        this.nome = nome;
        this.telefone = telefone;
        this.cpf = cpf;
        this.totalPessoas = totalPessoas;
    }
}
